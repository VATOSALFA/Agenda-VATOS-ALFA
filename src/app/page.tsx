'use client';

import Image from "next/image";

import { Button } from "@/components/ui/button";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { useState, useEffect } from "react";
import { Loader2, AlertCircle, Eye, EyeOff, Calendar } from "lucide-react";
import { useToast } from "@/hooks/use-toast";
import { Alert, AlertTitle, AlertDescription } from "@/components/ui/alert";
import { FirebaseError } from "firebase/app";
import { useAuth } from "@/contexts/firebase-auth-context";
import { useRouter } from "next/navigation";
import { Checkbox } from "@/components/ui/checkbox";
import { sendPasswordResetEmail } from "firebase/auth";
import { auth } from "@/lib/firebase-client";

export default function LoginPage() {
    const [email, setEmail] = useState('');
    const [password, setPassword] = useState('');
    const [rememberMe, setRememberMe] = useState(false);
    const [showPassword, setShowPassword] = useState(false);
    const [isLoading, setIsLoading] = useState(false);
    const [error, setError] = useState<string | null>(null);
    const [isResettingPassword, setIsResettingPassword] = useState(false);
    const [resetEmail, setResetEmail] = useState('');
    const [resetMessage, setResetMessage] = useState('');

    const { toast } = useToast();
    const { user, loading, signInAndSetup, signInWithGoogle } = useAuth();
    const router = useRouter();

    useEffect(() => {
        // Redirect to /agenda if user is already logged in
        if (!loading && user) {
            router.replace('/agenda');
        }
    }, [user, loading, router]);


    if (loading || user) {
        return (
            <div className="flex justify-center items-center h-screen bg-muted/40">
                <Loader2 className="h-12 w-12 animate-spin text-primary" />
            </div>
        );
    }

    const handleLogin = async (e: React.FormEvent) => {
        e.preventDefault();
        setIsLoading(true);
        setError(null);

        if (!signInAndSetup) {
            setError("El servicio de autenticación no está disponible. Por favor, recarga la página.");
            setIsLoading(false);
            return;
        }

        try {
            await signInAndSetup(email, password, rememberMe);
            toast({ title: "¡Bienvenido!", description: "Has iniciado sesión correctamente." });
            router.push('/agenda');
        } catch (err: unknown) {
            console.error("Error de inicio de sesión:", err);
            if (err instanceof FirebaseError) {
                switch (err.code) {
                    case 'auth/user-not-found':
                    case 'auth/wrong-password':
                    case 'auth/invalid-credential':
                        setError("El correo o la contraseña son incorrectos.");
                        break;
                    case 'auth/too-many-requests':
                        setError("Demasiados intentos fallidos. Por favor, inténtalo más tarde.");
                        break;
                    default:
                        setError("Ocurrió un error inesperado. Por favor, inténtalo de nuevo.");
                }
            } else if (err instanceof Error && err.message.includes("ACCESS_DENIED")) {
                setError("Tu cuenta de Google está autenticada, pero tu correo no está registrado en el sistema. Contacta al administrador.");
            } else {
                setError("Ocurrió un error. Verifica tus credenciales o si tu cuenta tiene acceso.");
            }
        } finally {
            setIsLoading(false);
        }
    };

    const handlePasswordReset = async () => {
        if (!resetEmail) {
            setResetMessage('Por favor, ingresa tu correo electrónico.');
            return;
        }
        setIsResettingPassword(true);
        setResetMessage('');
        try {
            await sendPasswordResetEmail(auth, resetEmail);
            setResetMessage('Se ha enviado un correo para restablecer tu contraseña. Revisa tu bandeja de entrada.');
        } catch (error: any) {
            console.error("Password reset error:", error);
            if (error.code === 'auth/user-not-found') {
                setResetMessage('No se encontró ninguna cuenta con ese correo electrónico.');
            } else {
                setResetMessage('Ocurrió un error. Por favor, inténtalo de nuevo.');
            }
        } finally {
            setIsResettingPassword(false);
        }
    }

    return (
        <div className="flex flex-col items-center justify-center h-screen bg-muted/40">
            <div className="flex flex-col items-center mb-0 animate-in fade-in zoom-in duration-700 slide-in-from-bottom-4">
                <Image
                    src="/logo-vatos-alfa.png"
                    alt="Vatos Alfa Logo"
                    width={100}
                    height={100}
                    className="mb-0 drop-shadow-md"
                    priority
                />
                <h1 className="text-3xl font-bold text-slate-800 tracking-tight">VATOS ALFA</h1>
            </div>
            <Card className="w-full max-w-sm">
                <CardHeader className="text-center">
                    <CardTitle className="text-2xl">Iniciar Sesión</CardTitle>
                    <CardDescription>
                        Ingresa tu correo y contraseña para acceder a tu cuenta.
                    </CardDescription>
                </CardHeader>
                <CardContent>
                    {error && (
                        <Alert variant="destructive" className="mb-4">
                            <AlertCircle className="h-4 w-4" />
                            <AlertTitle>Error de Acceso</AlertTitle>
                            <AlertDescription>
                                {error}
                            </AlertDescription>
                        </Alert>
                    )}
                    {isResettingPassword === false && resetMessage && (
                        <Alert className="mb-4">
                            <AlertTitle>{resetMessage.includes('enviado') ? 'Correo Enviado' : 'Aviso'}</AlertTitle>
                            <AlertDescription>{resetMessage}</AlertDescription>
                        </Alert>
                    )}
                    <form onSubmit={handleLogin} className="space-y-4">
                        <div className="space-y-2">
                            <Label htmlFor="email">Correo Electrónico</Label>
                            <Input
                                id="email"
                                type="email"
                                placeholder="tu@email.com"
                                required
                                value={email}
                                onChange={(e) => setEmail(e.target.value)}
                            />
                        </div>
                        <div className="space-y-2">
                            <Label htmlFor="password">Contraseña</Label>
                            <div className="relative">
                                <Input
                                    id="password"
                                    type={showPassword ? "text" : "password"}
                                    required
                                    value={password}
                                    onChange={(e) => setPassword(e.target.value)}
                                />
                                <Button
                                    type="button"
                                    variant="ghost"
                                    size="icon"
                                    className="absolute top-0 right-0 h-full px-3"
                                    onClick={() => setShowPassword(!showPassword)}
                                >
                                    {showPassword ? <EyeOff className="h-4 w-4" /> : <Eye className="h-4 w-4" />}
                                </Button>
                            </div>
                        </div>
                        <div className="relative">
                            <div className="absolute inset-0 flex items-center">
                                <span className="w-full border-t" />
                            </div>
                            <div className="relative flex justify-center text-xs uppercase">
                                <span className="bg-background px-2 text-muted-foreground">O continúa con</span>
                            </div>
                        </div>
                        <Button
                            type="button"
                            variant="outline"
                            className="w-full"
                            onClick={async () => {
                                setIsLoading(true);
                                try {
                                    if (signInWithGoogle) {
                                        await signInWithGoogle();
                                        toast({ title: "¡Bienvenido!", description: "Has iniciado sesión con Google correctamente." });
                                        router.push('/agenda');
                                    }
                                } catch (error: any) {
                                    if (error.message?.includes("ACCESS_DENIED") || error.code === 'auth/user-not-found') {
                                        setError("Acceso denegado: Tu correo no está registrado como usuario en el sistema.");
                                    } else {
                                        console.error("Error Google Auth:", error);
                                        setError("Error al iniciar sesión con Google. Inténtalo de nuevo.");
                                    }
                                } finally {
                                    setIsLoading(false);
                                }
                            }}
                            disabled={isLoading}
                        >
                            <svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
                                <path
                                    d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                    fill="#4285F4"
                                />
                                <path
                                    d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                    fill="#34A853"
                                />
                                <path
                                    d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.84z"
                                    fill="#FBBC05"
                                />
                                <path
                                    d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                    fill="#EA4335"
                                />
                            </svg>
                            Entrar con Google
                        </Button>
                        <div className="flex items-center justify-between">
                            <div className="flex items-center space-x-2">
                                <Checkbox id="remember-me" checked={rememberMe} onCheckedChange={(checked) => setRememberMe(!!checked)} />
                                <Label htmlFor="remember-me" className="text-sm font-normal">Recordarme</Label>
                            </div>
                            <Button type="button" variant="link" className="px-0 h-auto text-sm" onClick={() => setIsResettingPassword(true)}>¿Olvidaste tu contraseña?</Button>
                        </div>
                        <Button type="submit" className="w-full" disabled={isLoading}>
                            {isLoading && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
                            Iniciar Sesión
                        </Button>
                    </form>
                </CardContent>
            </Card>
            {isResettingPassword && (
                <Card className="w-full max-w-sm mt-4">
                    <CardHeader className="text-center">
                        <CardTitle>Restablecer Contraseña</CardTitle>
                        <CardDescription>
                            Ingresa tu correo para enviarte un enlace de restablecimiento.
                        </CardDescription>
                    </CardHeader>
                    <CardContent>
                        {resetMessage && (
                            <Alert className="mb-4" variant={resetMessage.includes('encontró') ? 'destructive' : 'default'}>
                                <AlertCircle className="h-4 w-4" />
                                <AlertTitle>{resetMessage.includes('encontró') ? 'Error' : 'Aviso'}</AlertTitle>
                                <AlertDescription>{resetMessage}</AlertDescription>
                            </Alert>
                        )}
                        <div className="space-y-2">
                            <Label htmlFor="reset-email">Correo Electrónico</Label>
                            <Input
                                id="reset-email"
                                type="email"
                                placeholder="tu@email.com"
                                required
                                value={resetEmail}
                                onChange={(e) => setResetEmail(e.target.value)}
                            />
                        </div>
                        <div className="flex gap-2 mt-4">
                            <Button variant="outline" className="w-full" onClick={() => { setIsResettingPassword(false); setResetMessage(''); }}>
                                Volver a Iniciar Sesión
                            </Button>
                            <Button className="w-full" onClick={handlePasswordReset}>
                                {isLoading && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
                                Enviar
                            </Button>
                        </div>
                    </CardContent>
                </Card>
            )}
        </div>
    );
}
